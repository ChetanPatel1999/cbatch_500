//arthmatical operator in c language
#include<stdio.h>
void main()
{
 int a=12;
 float b=8;
 float ans;
 ans=a+b;
 printf("%d + %.2f = %.2f\n",a,b,ans);
 ans=a-b;
 printf("%d - %.2f = %.2f\n",a,b,ans);
 ans=a*b;
 printf("%d * %.2f = %.2f\n",a,b,ans);
 ans=a/b;
 printf("%d / %.2f = %.2f\n",a,b,ans);
  
}
